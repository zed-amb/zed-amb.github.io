/* isVowel() that takes a character (i.e. a string of length 1) and returns true if it is a vowel, false
otherwise. */

 export function isVowel(text) {
    if (text == "a" || text == "e" || text == "i" || text == "o" || text == "u") {
        return true;
    }

}
